const mangoose = require('mangoose');
const Schema = mangoose.Schema;

const CourseSchema = new Schema({
  name: {
    type: String,
    unique: true,
    required: true,
  },
  description: {
    type: String,
    required: true,
    trim: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Course = mangoose.model('Course', CourseSchema);
module.exports = Course;
